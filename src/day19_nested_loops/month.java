package day19_nested_loops;

public class month {
    public static void main(String[] args) {

        for (int i = 1; i <= 4; i++) {
            System.out.println("week: "+i);
            for (int j = 1; j <= 7; j++) {
                System.out.println("\tday: " +j);
            }

        }



    }
}
